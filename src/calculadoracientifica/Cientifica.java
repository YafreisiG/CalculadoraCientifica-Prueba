     // Operaciones de multiplicaciones 
    private void Operaciones (double valor1, double valor2){
        switch(TipoOperaciones){
            //Para la suma "+"
            case "+":
                resultado = valor1 + valor2;
                Casilla_Principal.setText(resultado + "");
                valor1 = Double.parseDouble(this.Casilla_Principal.getText());
                break;
                //Para la resta "-"
            case "-":
                resultado = valor1 - valor2;
                Casilla_Principal.setText(resultado + "");
                valor1 = Double.parseDouble(this.Casilla_Principal.getText());
                break;
                //Para la Multiplicacion "*"
                case "*":
                resultado = valor1 * valor2;
                Casilla_Principal.setText(resultado + "");
                valor1 = Double.parseDouble(this.Casilla_Principal.getText());
                break;
                //Para la division "/"
                case "/":
                if (valor2 == 0){
                    Casilla_Principal.setText(resultado + " ");
                    valor1 = Double.parseDouble(Casilla_Principal.getText());
                    break;
                }else{
                    resultado = valor1 / valor2;
                    Casilla_Principal.setText(resultado + " ");
                    valor1 = Double.parseDouble(Casilla_Principal.getText());
                    break;
                }
                
                case "potencia":
                resultado = Math.pow (valor1, valor2);
                Casilla_Principal.setText(resultado + " ");
                break;
                
                case "raizx":
                if (valor2 <=0){
                Casilla_Principal.setText("Error");
                break;
                }else {
                    Casilla_Secundaria.setText(valor2 + "yroot");
                    a = 1;
                    c = a/valor2;
                    Casilla_Principal.setText(Math.pow(valor1, c)+ " ");
                    break;
                }
        }
    }
